# Dynamic programming:
# useful when trying to optimize problem given a constraint
# divide problem into subproblems and solve them first then build up to solving the big problem
# uses grid to solve problems
# each cell in the grid is a subproblem
# the values in the cells are usually what we're tryig to optimize
# changing orders of the rows does not affect the final solution
# only works when each subproblem is discrete (when it doesn't depend on other subproblems)
# there's no single formula for dynamic programming
